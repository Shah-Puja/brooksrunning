                <ul>
                  <li>The 3 S's - Supporting,   Stretching, and Strengthening, along with ICE and REST, have been found   to be the simplest and most effective for these injuries. </li>
                  <li>Supporting the foot with   proper shoes and insoles, can prevent or help to eliminate the vast   majority of lower extremity problems due to faulty biomechanics. One of   the easiest and most effective solutions is to add a simple over the   counter insole that provides a forgiving support for both the arch and   heel. </li>
                  <li>Stretching of the hamstring, quad, calf and IT Band with help to decrease pressure at the kneecap. </li>
                  <li>Strengthening of the   quad, especially the VMO (vastus medialis oblique muscle) will help the   kneecap to glide more correctly through the groove at the knee joint. </li>
                  <li>Avoid downhill running or going up/down stairs. </li>
                  <li>Avoid exercises done with the knee bent unless being done as an isometric </li>
                  <li>Physical Therapy including exercise, ultrasound, iontophoresis and patellar mobilization </li>
