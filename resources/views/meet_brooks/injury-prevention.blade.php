            <li>The primary function is to provide stability to the lateral knee while standing </li>
            <li>It helps to maintain hip extension in standing and hip/knee flexion in running and walking </li>
            <li>The IT-Band moves   forward at the knee as the knee extends and slides backward at the knee   as the knee flexes, but is tense in both positions </li>
          </ul>
          <h5>Iliotibial Band Friction Syndrome</h5>
          <ul>
            <li>Inflammation where the band/tendon/fascia rubs across the distal lateral femur (outside, of bottom end, of thigh bone)</li>
            <li>Can be inflammation of the band/tendon, bursa under the tendon, or the periosteum(covering over bone) of femur</li>
          </ul>
          <h4>PRIMARY CAUSE</h4>
          <ul>
            <li>Excessive Pronation </li>
            <li>Pronation is a normal movement of the foot that helps the body to absorb shock and to adapt to different ground surfaces. </li>
            <li>In analyzing ones gait,   first contact is on the heel and outside of the foot; followed by a   shift of body weight continuing forward, toward the arch and toes. </li>
            <li>If the foot is weak or   tired and/or the footwear is not supportive, then the arch can flatten   more than normal, which is excessive pronation. </li>
            <li>Flattening of the arch   (excessive pronation) places pressure on the foot and causes rotation to   occur at the knee pulling on the IT Band. If the IT Band is not   stretched enough it can cause inflammation. </li>
            <li>This repetitive, excessive pronation, is the main contributor to many lower extremity, overuse injuries. </li>
          </ul>
          <h4>CONTRIBUTING FACTORS</h4>
          <ul>
            <li>With increasing age, often there is decreasing flexibility </li>
            <li>Any sudden change in activity, specifically, activities that increase weight bearing or pressure on the foot </li>
            <li>Changes in training - downhill running, running on banked surfaces, increasing training too quickly </li>
            <li>Anatomical abnormalities ¬ leg length discrepancies, bowlegs, and laxity of lateral knee ligament stability </li>
            <li>Underlying faulty pelvic mechanics </li>
          </ul>
