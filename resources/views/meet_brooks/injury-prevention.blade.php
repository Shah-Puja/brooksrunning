           <h4>DEFINITIONS</h4>
           <ul>
             <li>Plantar Fasciitis</li>
             <li>An inflammation of the connective tissue of the bottom of the foot.</li>
             <li>Plantar= bottom of the foot, Fascia=dense fibrous connective tissue, Itis=inflammation</li>
             <li>The plantar fascia attaches to the bottom of the heel bone and fans out to the toes.</li>
             <li>The plantar fascia is designed to support the foot and form the arch. It has very little elasticity and is very thick.</li>
           </ul>
           <h4>Heel Spurs</h4>
           <ul>
             <li>A heel spur is an abnormal growth of bone on the heel due to excessive stress or pulling where the plantar fascia attaches to the heel.</li>
             <li>The excessive tugging of the plantar fascia on the heel bone causes this excess of bone (bone spur) to grow in a pointed fashion.</li>
           </ul>
           <h4>PRIMARY CAUSE</h4>
           <ul>
             <li>Excessive Pronation</li>
             <li>Pronation is a normal movement of the foot that helps the body to absorb shock and adapt to different ground surfaces.</li>
             <li>In analyzing ones gait, first contact is on the heel and outside of the foot; followed by a shift of body weight continuing forward toward the arch and toes.</li>
             <li>If the foot is weak or tired and/or the footwear is not supportive, then the arch can flatten more than normal, which is excessive pronation.</li>
             <li>Flattening of the arch (excessive pronation) places pressure on the arch and stretches the plantar fascia (which supports the arch) and can create inflammation at the attachment on the heel.</li>
             <li>This repetitive, excessive pronation, is the main contributor to many lower extremity, overuse injuries, of which plantar fasciitis and heel spurs are the most common in the foot.</li>
           </ul>
           <h4>CONTRIBUTING FACTORS</h4>
           <ul>
             <li>Age-With increasing age, often there is decreasing flexibility.</li>
             <li>Any sudden change in activity, specifically activities that increase weight bearing or pressure on the foot.</li>
             <li>Changes in training - Increased toe running, speed of running or hill running can add stress to the feet.</li>
             <li>Flat, pronated feet or rigid, high arched feet may be more prone to problems.</li>
             <li>Sudden increase in body weight (such as pregnancy) may also add strain.</li>
             <li>Poor support in the shoes being worn and/or the poor support inside the shoes can add to the stress on the foot.</li>
             <li>Biomechanical changes in the foot can cause increases in pronation (see Primary Causes).</li>
           </ul>
           <h4>TREATMENT <span>- ADVICE GIVEN MOST OFTEN IN CURRENT LITERATURE</span></h4>
           <ul>
             <li>The 3S's - Supporting, Stretching, and Strengthening, along with ICE and REST,have been found to be the simplest and most effective treatment for these injuries.</li>
             <li>Supporting the foot with proper shoes and insoles, can prevent or eliminate the vast majority of foot related problems.</li>
             <li>Stretching of the calf, Achilles tendon and foot can help or eliminate the majority of plantar fasciitis problems.</li>
             <li>Strengthening the muscles of the foot and ankle can assist in eliminating and avoiding these problems.</li>
             <li>Arch supports are recognized as the most successful remedy with stretching a close second.</li>
           </ul>
