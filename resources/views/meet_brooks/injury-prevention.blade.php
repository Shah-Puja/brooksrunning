              <h4>SYMPTOMS</h4>
              <ul>
                <li>Aching along front of shin, at beginning of or after activity </li>
                <li>Pain along inside (medial) part of lower leg </li>
                <li>Generally develops gradually over weeks or months </li>
                <li>May have swelling in lower leg (in area of pain) </li>
              </ul>
              <h4>DEFINITIONS</h4>
              <h5>Shin Splints</h5>
              <ul>
                <li>Common, umbrella term used to identify pain along the shin or front of lower leg. </li>
                <li>More specific names for this condition are based on the area of the pain and the anatomy involved (see below). </li>
                <li>Injury generally occurs as a result of overuse </li>
              </ul>
              <h5>Stress Fracture - posterior</h5>
              <ul>
                <li>Most often occurring on the tibia (shin bone) and along the bottom third of the lower leg </li>
                <li>Often undetectable on x-ray until 10-14 days after pain starts </li>
              </ul>
              <h5>Compartment Syndrome ¬ anterior or posterior</h5>
              <ul>
                <li>The four divisions of muscles   in the lower leg (anterior, lateral, posterior-superficial and deep)   are each covered by thick tissue called fascia that surround the muscles   completely </li>
                <li>During exercise, muscle   volume increases by 20%, increasing pressure within each compartment.   Such pressure can affect blood vessels and nerves in the lower leg   potentially causing pain and damage to tissue and nerves </li>
              </ul>
              <h5>Tibial Periostitis - posterior</h5>
              <ul>
                <li>An inflammation of or trauma to the covering of the bone in shin (periostium) </li>
                <li>Over-exertion causes small tears of the muscle from the covering of the bone (periostium) </li>
                <li>Pain is most pronounced in the lower 3rd of the posterior tibia </li>
              </ul>
              <h5>Medial Tibial Stress Syndrome ¬ posterior</h5>
              <ul>
                <li>Stress to the muscles along the front medial side of the shin </li>
                <li>Generally occurring along the bottom third of the inside of tibia (shin) </li>
              </ul>
              <h4>PRIMARY CAUSE</h4>
              <ul>
                <li>Excessive Pronation </li>
                <li>Pronation is a normal   movement of the foot, that allows the arch to flatten to a degree, which   helps the body to absorb and adapt to different ground surfaces. </li>
                <li>In analyzing ones gait,   first contact is on the heel and outside of the foot; followed by a   shift of body weight continuing forward, toward the arch and toes. </li>
                <li>If the foot is weak or   tired and/or the footwear is not supportive, then the arch can flatten   more than normal, which is excessive pronation. </li>
                <li>Flattening of the arch   (excessive pronation) places pressure on the arch and can cause some   rotation into the lower leg. This repetitive movement can cause overuse   problems from the foot to the back. </li>
                <li>If excessive pronation   occurs from lack of support, then, increased stresses can be placed on   the lower leg and contribute to overuse problems </li>
              </ul>
              <h4>CONTRIBUTING FACTORS</h4>
              <ul>
                <li>Muscular imbalances of lower leg (calf muscles and anterior leg muscles) </li>
                <li>Insufficient shock absorption </li>
                <li>Poor Biomechanics/Improper foot positioning while running </li>
                <li>Worn out or inappropriate shoes (shoes should typically be replaced after 300-500 miles) </li>
                <li>Sudden increase in exercise or running (too much-too soon) </li>
                <li>Incorrect individual training plan </li>
                <li>Flat pronated feet </li>
              </ul>
              <h4>TREATMENT <span>- ADVICE GIVEN MOST OFTEN IN LITERATURE</span></h4>
              <ul>
                <li>The 3 S's - Supporting,   Stretching, and Strengthening - along with ICE and REST have been found   to be the simplest and most effective treatment for these injuries. </li>
                <li>Stretching of the calf ( both gastroc and soleus muscles) and achilles tendon. </li>
                <li>Strengthening of the anterior leg muscles (that pull the foot and toes up). </li>
                <li>Supporting the foot with   proper shoes and insoles, can prevent and eliminate the vast majority   of lower leg problems due to overuse. </li>
                <li>Physical therapy including massage, ultrasound and exercises </li>
              </ul>
