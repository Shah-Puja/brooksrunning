	      	Achilles Tendon Injuries
      </label>
      <div class="accordian-content">
         <div class="contents-wrapper">
     		<div class="about-container--info">
     			<h4>SYMPTOMS</h4>
          <ul class="bold">
            <li>Pain along the achilles tendon during/after activity</li>
            <li>Swelling over the distal 1-3 inches of the Achilles tendon</li>
            <li>Tender to touch</li>
            <li>Pain associated with or when trying to raise up on the toes, or with stretching of the calf/Achilles tendon</li>
            <li>Limited range of motion and stiffness</li>
          </ul>
          <h4>DEFINITIONS</h4>
          <h5>Achilles Tendonitis</h5>
          <ul>
            <li>An inflammation of the tendon that connects the calf muscles to the heel bone (calcaneus)</li>
            <li>May be small tears in the tendon from overuse</li>
          </ul>
          <h5>Achilles rupture</h5>
          <ul>
            <li>Weakening of the collagen fibers of the tendon leading to partial tearing or rupture (complete tear) of the tendon away from the heel</li>
            <li>Rupture noted by inability to raise up on toes or push off with ball of foot in walking</li>
          </ul>
          <h5>Tenosynovitis</h5>
          <ul>
            <li>Inflammation of the vascular sheath that covers the Achilles tendon</li>
            <li>May even be caused by the shoe counter rubbing on the heel</li>
          </ul>
          <h4>PRIMARY CAUSE</h4>
          <h5>Excessive Pronation</h5>
          <p>Pronation is a normal movement of the foot, that allows the arch to flatten to a degree, which helps the body to absorb shock and adapt to different ground surfaces. In analyzing ones gait, first contact is on the heel and outside of the foot; followed by a shift of body weight continuing forward toward the arch and toes. If the foot is weak or tired and/or the footwear is not supportive, then the arch can flatten more than normal, which is excessive pronation. Flattening of the arch (excessive pronation) places pressure on the arch and stretches the plantar fascia (which supports the arch) and can create inflammation at the attachment on the heel.</p>
          <p>This repetitive, excessive pronation, is the main contributor to many lower extremity, overuse injuries. As the achilles tendon attaches on the back of the heel, and if the the foot is in excessive pronation, there is an increased torque or pull on the tendon, which can cause inflammation.</p>
          <h4>CONTRIBUTING FACTORS</h4>
          <ul>
            <li>Tight calf muscles ( the gastroc and/or soleus) and tight achilles tendon.</li>
            <li>Often with increasing age, there is decreasing flexibility</li>
            <li>Increasing the amount or intensity of training — uphill running, or stair climbing</li>
            <li>Flat pronated feet add stress to the soleus and rigid, high arched feet add stress to the gastroc</li>
            <li>Poor support on the inside of a shoe or in the shoe’s foundation/upper can add to the stress on the foot — thus increasing calf stress</li>
            <li>Change in the heel height of one’s shoes (from training shoes to racing flats, heels to flats etc.)</li>
          </ul>
          <h4>TREATMENT <span>- ADVICE GIVEN MOST OFTEN IN CURRENT LITERATURE</span></h4>
          <ul>
            <li>The 3’s — Stretching, Strengthening, and Supporting, along with ICE and REST have been found to be the simplest and most effective treatment for these injuries</li>
            <li>Stretching of the calf (both gastroc and soleus) muscles and Achilles tendon can help eliminate or prevent many problems with the achilles tendon.</li>
            <li>Strengthening of the calf muscles once the inflammation is gone, can help prevent further injury.</li>
            <li>Avoid hill or stair running until symptoms disappear.</li>
            <li>Supporting the foot with the proper shoes and insoles, can prevent or eliminate the vast majority of increased stresses on the lower extremity.</li>
            <li>Make sure that the heel collar does not dig into the achilles. Look for a cut out of the heel (heel notch)</li>
          </ul>
